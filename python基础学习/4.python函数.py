'''Python 函数
函数是一种仅在调用时运行的代码块。
您可以将数据（称为参数）传递到函数中。
函数可以把数据作为结果返回。
1.创建函数
在 Python 中，使用 def 关键字定义函数
2.调用函数
如需调用函数，请使用函数名称后跟括号
3.参数
信息可以作为参数传递给函数。
参数在函数名后的括号内指定。您可以根据需要添加任意数量的参数，只需用逗号分隔即可。
下面的例子有一个带参数（fname）的函数。当调用此函数时，我们传递一个名字，在函数内部使用它来打印全名
4.以 List 传参
您发送到函数的参数可以是任何数据类型（字符串、数字、列表、字典等），并且在函数内其将被视为相同数据类型。
例如，如果您将 List 作为参数发送，它到达函数时仍将是 List(列表)
5.关键字参数
您还可以使用 key = value 语法发送参数。
参数的顺序无关紧要。
6.任意参数
如果您不知道将传递给您的函数多少个参数，请在函数定义的参数名称前添加 *。
这样，函数将接收一个参数元组，并可以相应地访问各项
'''

#5.关键字参数
def my_function(child3, child2, child1):
  print("The youngest child is " + child3)
my_function(child1 = "Phoebe", child2 = "Jennifer", child3 = "Rory")

#6.任意参数(如果参数数目未知，请在参数名称前添加 *)
def my_function(*kids):
  print("The youngest child is " + kids[2])

my_function("Phoebe", "Jennifer", "Rory")

'''递归
Python 也接受函数递归，这意味着定义的函数能够调用自身。递归是一种常见的数学和编程概念。它意味着函数调用自身。
这样做的好处是可以循环访问数据以达成结果。开发人员应该非常小心递归，因为它可以很容易地编写一个永不终止的，或者使用过量内存或处理器能力的函数。
但是，在被正确编写后，递归可能是一种非常有效且数学上优雅的编程方法。在这个例子中，tri_recursion() 是我们定义为调用自身 ("recurse") 的函数。
我们使用 k 变量作为数据，每次递归时递减（-1）。当条件不大于 0 时（比如当它为 0 时），递归结束。
对于新的开发人员来说，可能需要一些时间来搞清楚其工作原理，最好的方法是测试并修改它
'''
def tri_recursion(k):
  if(k>0):
    result = k+tri_recursion(k-1)
    print(result)
  else:
    result = 0
  return result

print("\n\nRecursion Example Results")
tri_recursion(6)

'''Python Lambda
1.lambda 函数是一种小的匿名函数。lambda 函数可接受任意数量的参数，但只能有一个表达式。lambda 函数可接受任意数量的参数
2.为何使用 Lambda 函数？
当您把 lambda 用作另一个函数内的匿名函数时，会更好地展现 lambda 的强大能力。
'''
#一个 lambda 函数，它把参数 a、b 和 c 相加并打印结果：
x = lambda a, b, c : a + b + c
print(x(5, 6, 2))

'''
1. 闭包变量与调用参数的作用域隔离
a.闭包变量（如 n）
在函数定义时通过外部作用域捕获（如 myfunc(2) 中的 2）
被封装在函数对象内部，不属于调用时的参数
生命周期与函数对象一致，调用时无法直接修改（如 mydoubler(n=11) 会报错）
类比：像藏在函数内部的“固定配置值”，调用时不可见但会被使用。
b.调用参数（如 a）
在函数调用时显式传入（如 mydoubler(11) 中的 11）
绑定到函数定义中的第一个未绑定的参数（即 lambda a: ... 中的 a）
类比：像往函数这个“黑盒子”里投入的“输入值”，每次调用可不同。
2. 参数绑定的优先级与执行顺序
a.绑定规则
调用时传入的参数（如 11）优先绑定到函数定义的参数（如 a）
b.闭包变量（如 n）在函数定义时已确定，调用时不再参与参数传递
若尝试通过关键字参数修改闭包变量（如 mydoubler(n=3)），会因 n 不是函数参数而报错。

特性	          闭包变量（如 n）	                            调用参数（如 a）
来源	      函数定义时通过外部作用域捕获（如 myfunc(2)）	函数调用时显式传入（如 mydoubler(11)）
作用域	  封装在函数对象内部，属于闭包环境	            属于函数定义时的参数列表
生命周期	  与函数对象共存亡（定义时确定，不可修改）	        每次调用独立存在（调用时绑定，调用后释放）
修改方式	  不可直接修改（非函数参数）	                    通过调用传入不同值（如 a=11、a=20）
类比	      函数内部的“固定配置”（如乘法因子 2）	        函数的“动态输入”（如被乘数 11）

调用时传入的参数（如 11）优先绑定到函数定义的参数（如 lambda a: ... 中的 a）。
闭包变量（如 n）在函数对象创建时已确定，调用时不再参与参数传递。
错误示例：mydoubler(n=3) 会报错，因为 n 不是函数参数。
'''
#假设您有一个带一个参数的函数定义，并且该参数将乘以未知数字
def myfunc(n):
    return lambda a: a * n  # 1. 闭包捕获 n=2

mydoubler = myfunc(2)       # 2. 创建函数对象，内部记住 n=2
result = mydoubler(11)      # 3. 调用时传入 11 → 绑定到 a
                            # 4. 执行 a * n → 11 * 2 = 22

